# –°–æ–∑–¥–∞–π—Ç–µ —Å—Ç—Ä–æ–∫—É, –≤ –∫–æ—Ç–æ—Ä–æ–π –Ω–∞–ø–∏—Å–∞–Ω, –∫–∞–∫–æ–π-—Ç–æ —Ç–µ–∫—Å—Ç. –ü—Ä–∏–º–µ—Ä:
# We are not what we should be!
# We are not what we need to be.
# But at least we are not what we used to be
#  (Football Coach)
my_string = "We! are not what we should be!\nWe are not what we need to be.\nBut at least we are not what we used to be\n (Football Coach)"

# –ü–æ—Å—á–∏—Ç–∞–π—Ç–µ —Å–∫–æ–ª—å–∫–æ —Å–ª–æ–≤ –≤ —Ç–µ–∫—Å—Ç–µ

print(len(my_string.split()))
# –£–¥–∞–ª–∏—Ç–µ –∑–Ω–∞–∫–∏ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è –≤ —Å–ø–∏—Å–∫–µ —Å–ª–æ–≤ (–ø—Ä–æ–π–¥–∏—Ç–µ—Å—å —Ü–∏–∫–ª–æ–º –≤—Å–µ —Å–ª–æ–≤–∞ –∏ —É–¥–∞–ª–∏—Ç–µ –º–µ—Ç–æ–¥–æ–º strip –∑–Ω–∞–∫–∏ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è)

my_string2 = my_string.split()

for item in my_string2:
    my_string2[my_string2.index(item)] = item.strip(",.!()\n")

print(my_string2)

# –í—ã–≤–µ–¥–∏—Ç–µ —Å–ª–æ–≤–∞ –≤ –∞–ª—Ñ–∞–≤–∏—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ (–Ω–∞–π–¥–∏—Ç–µ –º–µ—Ç–æ–¥ —Å–ø–∏—Å–∫–∞, –∫–æ—Ç–æ—Ä—ã–π —Å–æ—Ä—Ç–∏—Ä—É–µ—Ç)
my_list = sorted(my_string2)
for item in my_list:
    print(item, end=" ")

# –£—Å–ª–æ–∂–Ω—ë–Ω–Ω–æ–µ * (–º–æ–∂–Ω–æ —Å–Ω–∞—á–∞–ª–∞ –µ–≥–æ –Ω–µ –¥–µ–ª–∞—Ç—å):
# –ü–æ—Å—á–∏—Ç–∞—Ç—å, —Å–∫–æ–ª—å–∫–æ —Ä–∞–∑ –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è –∫–∞–∂–¥–æ–µ —Å–ª–æ–≤–æ.
# (–ü–æ–¥—Å–∫–∞–∑–∫–∞: —Å–æ–∑–¥–∞–≤–∞—Ç—å —Å–ª–æ–≤–∞—Ä—å, –≥–¥–µ –∫–ª—é—á–∏ ‚Äî —ç—Ç–æ —Å–ª–æ–≤–∞ –∏–∑ —Ç–µ–∫—Å—Ç–∞,
# –∞ –≤ –∑–Ω–∞—á–µ–Ω–∏—è—Ö –ø–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ ¬´–≤—Å—Ç—Ä–µ—á–∞–Ω–∏–π¬ª —ç—Ç–æ–≥–æ —Å–ª–æ–≤–∞)
# –°–ª–æ–≤–∞ —Å –±–æ–ª—å—à–æ–π –±—É–∫–≤—ã –∏ —Å –º–∞–ª–µ–Ω—å–∫–æ–π —ç—Ç–æ –≤—Å–µ —Ä–∞–≤–Ω–æ –æ–¥–Ω–æ –∏ —Ç–æ–∂–µ —Å–ª–æ–≤–æ üòä

my_dict = dict()
for item in my_list:
    my_list[my_list.index(item)] = item.upper()
    if my_dict.get(item, 0) == 0:
        my_dict[item] = 1
    else:
        my_dict[item] += 1

print("\n", my_dict)
